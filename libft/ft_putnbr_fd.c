#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	res[10];
	int		i;

	i = 0;
	if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	if (n < 0 && n != -2147483648)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	while (n > 0)
	{
		res[i++] = '0' + (n % 10);
		n /= 10;
	}
	if (i != 0 && n != -2147483648)
	{
		while (--i >= 0)
			ft_putchar_fd(res[i], fd);
	}
	else if (i == 0 && n != -2147483648)
		ft_putchar_fd('0', fd);
}
