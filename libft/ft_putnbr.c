#include "libft.h"

void	ft_putnbr(int nb)
{
	char	res[10];
	int		i;

	i = 0;
	if (nb == -2147483648)
		ft_putstr("-2147483648");
	if (nb < 0 && nb != -2147483648)
	{
		ft_putchar('-');
		nb = -nb;
	}
	while (nb > 0)
	{
		res[i++] = '0' + (nb % 10);
		nb /= 10;
	}
	if (i != 0 && nb != -2147483648)
	{
		while (--i >= 0)
			ft_putchar(res[i]);
	}
	else if (i == 0 && nb != -2147483648)
		ft_putchar('0');
}
