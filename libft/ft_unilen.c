#include "libft.h"

size_t	ft_unilen(unsigned char *str)
{
	int		i;

	i = 0;
	while (str[0] != '\0')
	{
		if (str[0] > 191 || str[0] < 128)
			i++;
		str++;
	}
	return (i);
}
